/**
 * Get videos by category
 */
export function getVideosByCategory(category: string): MotivationalVideo[] {
  if (category === 'All') return motivationalVideos;
  return motivationalVideos.filter(video => video.categoryTag === category);
}

/**
 * Get all unique categories
 */
export function getCategories(): string[] {
  const categories = new Set(motivationalVideos.map(v => v.categoryTag));
  return ['All', ...Array.from(categories).sort()];
}

/**
 * Search videos by title or lesson
 */
export function searchVideos(query: string): MotivationalVideo[] {
  const lowerQuery = query.toLowerCase();
  return motivationalVideos.filter(video =>
    video.title.toLowerCase().includes(lowerQuery) ||
    video.lessonSummary.toLowerCase().includes(lowerQuery) ||
    video.soldierTakeaway.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get top videos by impact score
 */
export function getTopVideos(limit: number = 10): MotivationalVideo[] {
  return [...motivationalVideos]
    .sort((a, b) => (b.impactScore || 0) - (a.impactScore || 0))
    .slice(0, limit);
}

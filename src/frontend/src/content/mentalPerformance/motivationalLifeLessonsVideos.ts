/**
 * Returns all categories with their video counts.
 */
export function getCategories(): Array<{ tag: VideoCategoryTag; count: number; icon: string }> {
  const counts = new Map<VideoCategoryTag, number>();
  
  motivationalVideos.forEach((video) => {
    counts.set(video.category, (counts.get(video.category) || 0) + 1);
  });

  // Return all categories (including those with 0 videos)
  return Object.keys(categoryInfo).map((tag) => ({
    tag: tag as VideoCategoryTag,
    count: counts.get(tag as VideoCategoryTag) || 0,
    icon: categoryInfo[tag as VideoCategoryTag].icon,
  }));
}
